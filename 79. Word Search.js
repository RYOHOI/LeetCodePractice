/* Given a 2D board and a word, find if the word exists in the grid.

The word can be constructed from letters of sequentially adjacent cell, where "adjacent" cells are those horizontally or vertically neighboring. The same letter cell may not be used more than once.

For example,
Given board =

[
  ['A','B','C','E'],
  ['S','F','C','S'],
  ['A','D','E','E']
]

word = "ABCCED", -> returns true,
word = "SEE", -> returns true,
word = "ABCB", -> returns false. */

/**
 * @param {character[][]} board
 * @param {string} word
 * @return {boolean}
 */
var exist = function(board, word) {
  let firstChar = word[0]
  let maxRow = board.length
  let maxCol = board[0].length
  let flag = false
  for (let i = 0; i < maxRow; i++) {
    for (let j = 0; j < maxCol; j++) {
      explore(i, j, 0)
    }
  }
  return flag

  function explore(rowNo, colNo, index) {
    if (flag ||
      rowNo < 0 || rowNo >= maxRow || colNo < 0 || colNo >= maxCol ||
      board[rowNo][colNo] != word[index] ||
      board[rowNo][colNo] == '.') return

    if (index == word.length - 1) {
      flag = true
      return
    }

    board[rowNo][colNo] = '.'
    explore(rowNo + 1, colNo, index + 1)
    explore(rowNo - 1, colNo, index + 1)
    explore(rowNo, colNo + 1, index + 1)
    explore(rowNo, colNo - 1, index + 1)
    board[rowNo][colNo] = word[index]
  }
};

